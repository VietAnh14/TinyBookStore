    private void ButtonLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonLuuActionPerformed
        GiftDTO Gift = new GiftDTO();
        KhachHangDTO KH = new KhachHangDTO();
        bindingGift(Gift);
        bindingKH(KH);
        /*if (KhachHangBLL.checkValid(KH)) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
        }*/

        switch (flag) {
            case 1: {
                if (GiftBLL.insertGift(Gift)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }

                break;
            }

            case 2: {
                if (GiftBLL.updateGift(Gift)) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
                }

                break;
            }
            case 3:{
                Integer A = 0;
                A = Integer.parseInt(jTextDTL.getText().toString()) - Integer.parseInt(jMucDiem.getText().toString());
                if(A<0)
                {
                    JOptionPane.showMessageDialog(this, "Điểm tích lũy của khách hàng không đủ");
                }
                else{
                    if(KhachHangBLL.updateDTL(KH))
                    {
                        JOptionPane.showMessageDialog(this, "Cập nhật thành công");}
                    else
                    {
                        JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
                    }
                }
                break;
            }

            default:
            break;
        }

        bindingTableFormDoiQuaTL.bindingtbQua(jTableQua, GiftBLL.getAll());
        controlTxb(false);
    }//GEN-LAST:event_ButtonLuuActionPerformed

