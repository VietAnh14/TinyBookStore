     private void controlTxb(boolean control) {
        ButtonLuu.setEnabled(control);
        ButtonSua.setEnabled(!control);
        ButtonThem.setEnabled(!control);
        jTextMaQua.setEnabled(control);
        jTextTenQua.setEnabled(control);
        jTextMucDiem.setEnabled(control);
    }

    public void clearTxb() {
        jTextMaQua.setText("");
        jTextTenQua.setText("");
        jTextMucDiem.setText("");
    }

    public boolean bindingGift(GiftDTO Gift) {
        try{
            Gift.setMaQua(Integer.parseInt(jTextMaQua.getText()));
            Gift.SetTenQua(jTextTenQua.getText());
            Gift.setMucDiem(Integer.parseInt(jTextMucDiem.getText()));
        }
        catch(NumberFormatException e)
        {
            return false;
        }
        return true;
    
    
    }
    public boolean bindingKH(KhachHangDTO KH){
        try{
            KH.SetMaKH(Integer.parseInt(jTextMaQua.getText()));
            KH.SetHoten(jTextTenKH.getText());
            KH.SetDiemTichLuy(Integer.parseInt(jTextMucDiem.getText()));
        }
        catch(NumberFormatException e)
        {
            return false;
        }
        return true;
    }
