    private void jButtonLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLuuActionPerformed
        CongTyDTO congTy = new CongTyDTO();
        if (!bindingCty(congTy)) {
            JOptionPane.showMessageDialog(this, "Thông tin nhập không đúng quy định");
            return;
        }
        /*if (NhanVienBLL.checkValid(NV)) {
            JOptionPane.showMessageDialog(this, "Thông tin không hợp lệ");
        }*/

        switch (flag) {
            case 1: {
                if (congTyBll.insertCty(congTy)) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }

                break;
            }

            case 2: {
                if (congTyBll.updateCty(congTy)) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
                }

                break;
            }
            
            default:
                break;
        }

        ArrayList<CongTyDTO> listCongTy = new ArrayList<>();
        if (!congTyBll.getAll(listCongTy)) {
            JOptionPane.showMessageDialog(this, "Cant load table cong ty");
        }
        
       bindingTableFormQLCongTy.bindingTbleCty(tblCty, listCongTy);
        controlTxb(false);
    }//GEN-LAST:event_jButtonLuuActionPerformed

