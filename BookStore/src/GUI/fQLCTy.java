    public boolean bindingCty(CongTyDTO cty) {
        try {
            cty.setMaCty(Integer.parseInt(txbMaCTy.getText()));
            cty.setTenCty(txbTenCty.getText());
            cty.setDiaChi(txbDiaChi.getText());
            String sdt = txbSDT.getText();
            Integer.parseInt(sdt);
            if (sdt.length() <  8 || sdt.length() > 10) {
                return false;
            }
            cty.setSDT(txbSDT.getText());
        } catch (NumberFormatException e) {
            return false;
        }
        
        return true;
    }
    
